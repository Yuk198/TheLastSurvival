import pygame
import Screen,Var,InitAssets

class Decoration(pygame.sprite.Sprite):
	def __init__(self, img, x, y):
		pygame.sprite.Sprite.__init__(self)
		self.image = img
		self.rect = self.image.get_rect()
		self.rect.midtop = (x + Var.TILE_SIZE // 2, y + (Var.TILE_SIZE - self.image.get_height()))
	#scroll
	def update(self):
		self.rect.x += Var.screen_scroll

